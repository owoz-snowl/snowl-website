// Language helper functions

let _currentLang = localStorage.getItem('snowl_lang') || 'en';

function setLang(lang) {
  if (!i18n[lang]) return;
  _currentLang = lang;
  localStorage.setItem('snowl_lang', lang);
  updatePage(lang);
  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    var l = btn.getAttribute('data-lang') || '';
    if (l === lang) {
      btn.classList.add('active');
    } else {
      btn.classList.remove('active');
    }
  });
  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang: lang } }));
}

function getLang() {
  return _currentLang;
}

function updatePage(lang) {
  var t = i18n[lang];
  if (!t) return;
  // Text content
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.textContent = t[key];
    }
  });
  // Placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-placeholder');
    if (t[key] !== undefined) {
      el.placeholder = t[key];
    }
  });
  // Title attributes
  document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-title');
    if (t[key] !== undefined) {
      el.title = t[key];
    }
  });
  // HTML lang
  document.documentElement.lang = lang;
}

document.addEventListener('DOMContentLoaded', function() {
  var savedLang = localStorage.getItem('snowl_lang');
  if (savedLang && i18n[savedLang]) {
    _currentLang = savedLang;
  }
  updatePage(_currentLang);
  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    var lang = btn.getAttribute('data-lang');
    if (lang) {
      btn.addEventListener('click', function() { setLang(lang); });
    }
  });
});
